@dp.callback_query(lambda c: c.data.startswith('page_'))
async def handle_pagination(callback: types.CallbackQuery):
    try:
        _, category, page = callback.data.split('_')
        page = int(page)
        products = get_products(category)
        products_per_page = 5
        pages = [products[i:i + products_per_page] for i in range(0, len(products), products_per_page)]
        
        if page < 0 or page >= len(pages):
            await callback.answer("–≠—Ç–æ –ø–æ—Å–ª–µ–¥–Ω—è—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞")
            return
        
        keyboard = InlineKeyboardMarkup(row_width=2)
        for product in pages[page]:
            keyboard.add(InlineKeyboardButton(
                text=f"{product['name']} - {product['price']} ‚ÇΩ",
                callback_data=f"product_{product['id']}"
            ))
        
        # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏
        nav_buttons = []
        if page > 0:
            nav_buttons.append(InlineKeyboardButton(text="‚¨ÖÔ∏è", callback_data=f"page_{category}_{page-1}"))
        if page < len(pages) - 1:
            nav_buttons.append(InlineKeyboardButton(text="‚û°Ô∏è", callback_data=f"page_{category}_{page+1}"))
        if nav_buttons:
            keyboard.add(*nav_buttons)
        
        await callback.message.edit_text(
            f"üõç –¢–æ–≤–∞—Ä—ã –≤ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ {category}:\n\n"
            f"–°—Ç—Ä–∞–Ω–∏—Ü–∞ {page + 1} –∏–∑ {len(pages)}",
            reply_markup=keyboard
        )
        await callback.answer()
    except Exception as e:
        logger.error(f"Error in handle_pagination: {e}")
        await callback.answer("–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.")

